<div class="verein-satzung">
  <h2>Satzung vom 6.Juni 2000</h2>
  <div>
    <h3>§1 Name und Sitz</h3>
    <p>
      Der Verein hat den Namen „Kindertagesstätten an den Trierer Hochschulen
      e.V.“. Im Vereinsregister ist der Verein unter der Nummer 1459
      eingetragen.
    </p>
  </div>
  <div>
    <h3>§2 Aufgaben</h3>
    <p>
      Der Verein verfolgt als freier Träger der Jugendhilfe ausschließlich und
      unmittelbar gemeinnützige Zwecke. Er errichtet und betreibt
      Kindertagesstätten, dazu gehören:
    </p>
    <ol>
      <li>
        Kinderkrippen für Kinder im Alter von ungefähr 3 Monaten bis zu ungefähr
        3 Jahren,
      </li>
      <li>
        Kindergärten für Kinder vom vollendeten 3 Lebensjahr bis zum Beginn der
        Schulpflicht,
      </li>
      <li>
        Kinderhorte für Kinder im Primarschulalter. In den Einrichtungen können
        Kinder der Hochschulangehörigen und Kinder aus dem Einzugsbereich von
        Trier aufgenommen werden. Die erzieherische Förderung der Kinder soll
        unter wissenschaftlicher Beratung und in enger Zusammenarbeit mit den
        Eltern der Kinder erfolgen.
      </li>
    </ol>
  </div>
  <div>
    <h3>$3 Mitglieder</h3>
    <p>
      Ordentliches Mitglied kann jede/r werden die/der die Aufgaben des Vereins
      unterstützt und sich zur Zahlung des von der Mitgliederversammlung
      festgesetzten Mitgliedsbeitrages verpflichtet.
    </p>
    <p>
      Förderndes Mitglied kann jede/r werden, der/ die an Stelle des
      Mitgliedsbeitrages dem Verein jährlich einen Beitrag von wenigstens €
      50,-- zur Verfügung stellt.
    </p>
    <p>
      Ehrenmitglied kann jede/r werden, der/die sich besondere Verdienste um die
      Belange des Vereins erworben hat.
    </p>
    <p>
      Der Antrag auf Mitgliedschaft in dem Verein ist schriftlich an den
      Vorstand zu richten. Die Mitgliedschaft endet durch schriftliche
      Austrittserklärung gegenüber dem Vorstand, durch Tod oder durch
      Ausschluss. Der Ausschluss eines Mitgliedes erfolgt wegen Verstoßes gegen
      die Ziele des Vereins oder wiederholter Nichtentrichtung des
      Mitgliedsbeitrages. Über Aufnahme und Ausschluss von Mitgliedern sowie
      über die Ernennung von Ehrenmitgliedern entscheidet der Vorstand mit der
      Mehrheit der anwesenden Vorstandsmitglieder.
    </p>
  </div>
  <div>
    <h3>§4 Organe</h3>
    <p>Organe des Vereins sind die Mitgliederversammlung und der Vorstand.</p>
  </div>
  <div>
    <h3>$5 Mitgliederversammlung</h3>
    <p>
      Die ordentliche Mitgliederversammlung findet mindestens einmal im Jahr
      statt. Sie wird von dem/der Vorsitzenden des Vorstandes unter Angabe der
      Tagesordnung einberufen. Die Einberufung erfolgt schriftlich; sie muss
      mindestens zehn Tage vor Beginn der Mitgliederversammlung an die
      Mitglieder abgesendet worden sein. Auf Antrag von wenigstens einem Drittel
      der Mitglieder muss die/der Vorsitzende des Vorstandes zu einer
      außerordentlichen Mitgliederversammlung einladen.
    </p>
    <p>
      Die Mitgliederversammlung beschließt mit der Mehrheit der anwesenden
      Mitglieder, wählt den Vorstand des Vereins, erteilt dem scheidenden
      Vorstand Entlastung, beschließt über Vorlagen des Vorstandes sowie über
      Anträge der Mitglieder und setzt die Mitgliedsbeiträge fest. Über die
      Mitgliederversammlung ist eine Niederschrift zu fertigen, in der Beginn
      und Ende der Sitzung, die Namen der anwesenden Mitglieder sowie die
      gefassten Beschlüsse enthalten sein müssen. Zur Gültigkeit der
      Niederschrift bedarf es der Unterschrift der/des Vorsitzenden oder
      dessen/deren Stellvertreters/in und des/der Schriftführers/in.
    </p>
  </div>
  <div>
    <h3>$6 Vorstand</h3>
    <p>
      Dem Vorstand, der für zwei Jahre oder bis zur Wahl eines neuen Vorstandes
      im Amt ist, gehören an
    </p>
    <p>(1) als gewählte Mitglieder</p>
    <p>(a) ein/e Vorsitzende/r</p>
    <p>(b) ein/e stellvertretende/r Vorsitzende/r</p>
    <p>(c) ein/e Schriftführer/in</p>
    <p>(d) ein/e Schatzmeister/in</p>
    <p>
      (2) als geborene Mitglieder mit Stimmrecht, wenn sie Mitglieder des
      Vereins sind
    </p>
    <p>(a) Leiter/in der Tagesstätte Im Treff,</p>
    <p>(b) Leiter/in der Tagesstätte Schneidershof,</p>
    <p>
      Der Vorstand führt die Geschäfte des Vereins. Er tagt wenigstens einmal im
      Jahr und fällt seine Entscheidungen mit der Mehrheit der anwesenden
      stimmberechtigten Vorstandsmitglieder. Der Vorstand ist nur in Anwesenheit
      von mindestens vier Mitgliedern beschlussfähig. Hierzu müssen der/die
      Vorsitzende oder sein/ihre Stellvertreter/in gehören. Es gelten die
      Befangenheitsvorschriften des Verwaltungsverfahrensgesetzes.
    </p>
    <p>
      Über die Sitzungen des Vorstandes ist jeweils eine Niederschrift
      anzufertigen, in der Beginn und Ende der Sitzung, die Namen der anwesenden
      Mitglieder des Vorstandes sowie die gefassten Beschlüsse enthalten sein
      müssen. Zur Gültigkeit der Niederschrift bedarf es der Unterschrift
      der/des Vorsitzenden oder dessen/deren Stellvertreters/in oder des/der
      Schriftführers/in.
    </p>
    <p>
      Vorstand im Sinne des §26 des BGB ist der/die Vorsitzende, in dessen/deren
      Verhinderungsfall der/die stellvertretende/r Vorsitzende/r, im
      Verhinderungsfall des/der Vorsitzenden und des/der stellvertretenden
      Vorsitzenden der/die Schatzmeister/in oder der/die Schriftführer/in;
      jede/r der vier Vorgenannten ist allein zur Vertretung des Vereins
      berechtigt. Näheres regelt die Geschäftsordnung.
    </p>
    <p>
      Alle Vorstandsmitglieder sind bei der Wahrnehmung der Interessen des
      Vereins an die Gesetze des Bundeslandes Rheinland-Pfalz zur Ausführung des
      Kindertagesstättengesetzes gebunden.
    </p>
  </div>
  <div>
    <h3>§7 Beirat</h3>
    <p>
      Der Vorstand setzt zur Unterstützung seiner Arbeit zu Beginn seiner
      Amtszeit einen Beirat ein, dem jeweils ein Mitglied der Universität Trier,
      der Fachhochschule Trier und dem Studentenwerk Trier angehören muss. Es
      ist anzustreben ein/e Psycholog/in und ein/e Pädagoge/in aus dem Kreis der
      Professoren/innen, Hochschulassistenten/innen und wissenschaftlichen
      Mitarbeiter/innen der Universität in den Beirat aufzunehmen.
    </p>
  </div>
  <div>
    <h3>§8 Gemeinnützigkeiten</h3>
    <p>
      Die Gemeinnützigkeit des Vereins folgt den Bestimmungen des dritten
      Abschnitts („Steuerbegünstigte Zwecke“) der „Abgabenordnung (AO 1977)“.
      Insbesondere werden die Bestimmungen des §55 („Selbstlosigkeit“), des §56
      („Ausschließlichkeit“) sowie des §57 („Unmittelbarkeit“) beachtet.
    </p>
    <p>Der Verein verfolgt seine Aufgaben selbstlos, das heißt:</p>
    <p>
      (1) Mittel des Vereins werden nur für satzungsgemäße Zwecke verwendet. Die
      Mitglieder erhalten keine Gewinnanteile und in ihrer Eigenschaft als
      Mitglieder auch keine sonstigen Zuwendungen aus Mitteln des Vereins.
    </p>
    <p>
      (2) Die Mitglieder erhalten bei ihrem Ausscheiden oder Aufhebung des
      Vereins nicht mehr als ihre eingezahlten Kapitalanteile und den gemeinen
      Wert ihrer geleisteten Sacheinlagen zurück.
    </p>
    <p>
      (3) Der Verein begünstigt keine Person durch Ausgaben, die dem Zwecke des
      Vereins fremd sind oder durch unverhältnismäßig hohe Vergütungen.
    </p>
    <p>
      (4) Bei Auflösung oder Aufhebung des Vereins oder bei Wegfall seines
      bisherigen Zweckes wird das Vermögen des Vereins, soweit es die
      eingezahlten Kapitalanteile der Mitglieder und den gemeinen Wert der von
      den Mitgliedern geleisteten Sacheinlagen übersteigt, nur für
      steuerbegünstigte Zwecke verwendet (Grundsätze der Vermögensbindung).
      Diese Voraussetzung ist erfüllt, wenn das Vermögen mit Zustimmung des
      Finanzamtes einer anderen steuerbegünstigten Körperschaft oder einer
      Körperschaft des öffentlichen Rechts für steuerbegünstigte Zwecke
      übertragen wird.
    </p>
    <p>
      Der Verein verfolgt seine Aufgaben ausschließlich, das heißt: (5) Der
      Verein verfolgt nur seine steuerbegünstigten, satzungsmäßigen Zwecke.
    </p>
    <p>
      Der Verein verfolgt seine Aufgaben unmittelbar, das heißt: (6) der Verein
      verfolgt seine steuerbegünstigten, satzungsmäßigen Zwecke selbst oder
      durch Hilfspersonen, deren Wirken wie eigenes Wirken dem Verein anzusehen
      ist.
    </p>
  </div>
  <div>
    <h3>§9 Auflösung</h3>
    <p>
      Die Auflösung des Vereins kann durch die Mitgliederversammlung in
      Anwesenheit von wenigstens der Hälfte der Mitglieder mit
      Dreiviertelmehrheit beschlossen werden. Die Abkündigung einer
      Mitgliederversammlung mit dem Tagesordnungspunkt „Auflösung des Vereins“
      muss wenigstens vier Wochen vorher schriftlich an alle Mitglieder
      erfolgen.
    </p>
    <p>
      Ist bei einer ersten Mitgliederversammlung mit dem Tagesordnungspunkt
      „Auflösung des Vereins“ weniger als die Hälfte der Mitglieder anwesend, so
      genügt bei einer zweiten, unter Beachtung der gleichen Einladungsfrist
      anzukündigenden, Mitgliederversammlung mit dem selben Tagesordnungspunkt
      eine Dreiviertelmehrheit der Anwesenden zur Beschlussfassung.
    </p>
    <p>
      Im Falle der Auflösung des Vereins fällt – nach Abdeckung aller
      rechtlichen Verpflichtungen – das Vermögen an eine Institution gleicher
      Zielsetzung. Hierüber beschließt die auflösende Mitgliederversammlung.
    </p>
  </div>
  <div>
    <h3>§10 Änderung der Satzung</h3>
    <p>
      Diese Satzung kann mit einer Mehrheit von zwei Drittel der zu einer
      ordnungsgemäß eingeladenen Mitgliederversammlung erschienenen Mitglieder
      geändert werden.
    </p>
  </div>
</div>


