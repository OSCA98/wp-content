<div>
  <h2>Wer wir sind</h2>
  <p>
    Unsere erste Kindertagesstätte (KITA) nahm 1974 am damaligen Standort der
    Universität, dem Schneidershof, ihren Betrieb auf. Für eine
    Kindertagesstätte an der Universität Trier hatte sich vor allem der erste
    Dekan des Fachbereichs I der Universität, Prof. Dr. Günter Reinert,
    eingesetzt. Er betrieb die Gründung der „Vereinigung Kindertagesstätte an
    der Universität Trier e.V". Mit dem Umzug der Universität nach
    Trier-Tarforst kam im Sommer 1985 unser zweiter Standort im
    Stadtteilzentrum "Im Treff" hinzu. Im Februar 1992 erfolgte, durch die
    Verlagerung der Fachhochschule auf den Schneidershof die Namensänderung
    unserer Einrichtung. Aus der Uni-KITA wurde die Vereinigung
    „Kindertagesstätte an den Trierer Hochschulen e.V."
  </p>
  <p>
    Wie der Name Kindertagesstätte sagt, sind unsere Kindergärten
    Ganzzeitkindergärten, die auch die Betreuung der Kinder über Mittag
    einschließen.
  </p>
  <p>
    Die KITA Schneidershof ist im ehemaligen Direktorenwohnhaus der früheren
    Pädagogischen Hochschule (heute Fachhochschule) untergebracht. Direkt am
    Haus befindet sich ein großer Garten mit alten Obstbäumen. Da das Gelände
    der Fachhochschule am Rande des Weisshauswaldes liegt, bieten sich
    Tierpark und Wald als „Forschungsgelände" für die Kinder an. In der KITA
    Schneidershof ist Platz für 42 Kinder im Alter von 2 – 6 Jahren, die in
    zwei Ganztagsgruppen untergebracht sind.
  </p>
  <p>
    Unser zweiter Standort die KITA Im Treff befindet sich in unmittelbarer
    Nähe der Universität im Stadtteilzentrum, Tarforster Höhe. Hier können 76
    Kinder im Alter von 3 - 6 Jahren in drei Gruppen und 10 Kinder von 1-3 in
    der Krippengruppe ganztags betreut werden.
  </p>
  <p>
    Wir sind weltanschaulich und konfessionell neutral. Im Mittelpunkt unseres
    pädagogischen Konzepts steht das Kind als Individuum, das in seiner
    Entwicklung unseres Wohlwollens, unserer Förderung und Hilfe bedarf. Unser
    pädagogisches Konzept wird unter Berücksichtigung gesellschaftlicher
    Veränderungen stets reflektiert, überarbeitet und gegebenenfalls
    entsprechend verändert.
  </p>
  <p>
    Unter den Eltern sind die verschiedensten sozialen Schichten und
    weltanschaulichen Richtungen vertreten. Der Träger unserer Einrichtungen
    ist der Verein "Kindertagesstätte an den Trierer Hochschulen e.V.", der
    vom Finanzamt als gemeinnützig anerkannt ist.
  </p>
</div>

<div>
  <h2>Was wollen wir?</h2>
  <p>
    Für die Kinder ist die Kindergartenzeit ein wichtiger Lebensabschnitt. Wir
    wollen, dass dies eine schöne Zeit für sie ist. Unser Konzept ist nicht
    starr, sondern lebendig wie die Kinder, die unsere Einrichtung besuchen.
    Es berücksichtigt ihre verschiedenen Bedürfnisse und „lebt" mit ihnen.
    Jedes Kind hat seine eigene einzigartige Lebensgeschichte. Wir bieten ihm
    - zusätzlich zu seiner Familie - den Raum, sich mit all seiner
    Lebendigkeit auszuleben.
  </p>
  <p>
    In der offenen und schutzbietenden Atmosphäre unsrer KITAs können sich die
    Kinder entfalten; dabei erleben sie sich und die anderen mit Fröhlichkeit,
    aber auch mit Ängsten. Sie erfahren, was möglich ist und was nicht. Sie
    lernen, wie das „Leben“ funktioniert. Aber am meisten erfahren sie von
    sich selbst: Es ist ihnen möglich, ihre individuellen Gefühle wahrzunehmen
    und auszuleben. Und wie das geht, lernen sie in der Gemeinschaft. Sie
    lernen auch verschiedene Verhaltensweisen kennen und können Neues
    ausprobieren. Wir leben in wachsender Partnerschaft mit den Kindern und
    nehmen sie ernst. Wir verbringen täglich viele Stunden miteinander und
    erleben uns ähnlich einer „großen Familie", in der die Kinder die
    Geborgenheit finden, die sie unbedingt brauchen.
  </p>
  <p>
    Bei uns gestalten die Kinder den Alltag mit und übernehmen damit auch
    Verantwortung. Unsere Aufgabe besteht u.a. darin, die konkreten Interessen
    der Kinder durch gezielte Angebote zu unterstützen. Die Kinder zeigen uns
    dann, wie mit viel Phantasie aus „Nichts" etwas Bedeutendes werden kann.
    Daher stellen wir benötigtes Material nur in begrenztem Umfang zur
    Verfügung. Manchmal gehen wir dann auch auf „Schatzsuche" und sammeln das,
    was wir noch brauchen. Dadurch lernen wir ganz natürlich unsere Umgebung
    kennen. Es gibt immer wieder Neues zu entdecken und deshalb wissen wir
    jetzt auch, dass Eichhörnchen schneller sind als wir gucken können. Wir
    erleben aber auch wie gut es tut, in den Wald zu laufen, Wut heraus zu
    schreien und seinen ganzen Ärger da zu lassen. Unser Ziel ist, dass die
    Kinder die Umwelt bewusst erleben. Sie sammeln in ihrer nächsten Umgebung
    jede Menge an Erfahrung und lernen dabei, Verantwortung zu übernehmen. So
    entwickeln sie Bewusstsein für die Umwelt.
  </p>
  <p>
    Wichtig ist uns auch die Ernährung. Da wir eine Kindertagesstätte sind,
    hat das gemeinsame Frühstück und Mittagessen einen großen Stellenwert in
    unserer Arbeit. Gesunde Ernährung ist deshalb ein wichtiges Thema -
    genauso wie die Frage, ob blaue oder rote Smarties auf den
    Geburtstagskuchen gehören. Unsere Köchinnen kochen den Kindern seit Januar
    1997 täglich ein vollwertiges Mittagessen.
  </p>
  <p>
    Wir wollen auch in Zukunft unserem hohen Anspruch gerecht werden, uns in
    den KITAs als Begleiter unserer Kinder zu verstehen und dabei immer wieder
    zu erfahren, wie spannend es ist, mit ihnen in unseren Einrichtungen zu
    leben und gemeinsam viel Spaß dabei zu haben! Für die Erzieherlnnen
    bedeutet die Arbeit mit den Kindern, niemals Langeweile zu haben.
  </p>
